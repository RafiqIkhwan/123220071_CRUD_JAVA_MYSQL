    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            st = cn.createStatement();
            if (judul.getText().isEmpty() || genre.getText().isEmpty() || penulis.getText().isEmpty()
                    || penerbit.getText().isEmpty() || lokasi.getText().isEmpty() || stock.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if ("Simpan".equals(btnSimpan.getText())) {
                String cek = "SELECT * FROM dataperpus WHERE id = '" + id.getText() + "'";

                rs = st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Ups... judul buku ini sudah ada");
                } else {
                    String sql = "INSERT INTO dataperpus (judul, genre, penulis, penerbit, lokasi, stock) VALUES (?, ?, ?, ?, ?, ?)";
                    PreparedStatement insertStatement = cn.prepareStatement(sql);
                    insertStatement.setString(1, judul.getText());
                    insertStatement.setString(2, genre.getText());
                    insertStatement.setString(3, penulis.getText());
                    insertStatement.setString(4, penerbit.getText());
                    insertStatement.setString(5, lokasi.getText());
                    insertStatement.setString(6, stock.getText());
                    insertStatement.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    Bersih();
                    TampilData();
                }
            } else {
                // Jika ada logika lain saat tombol bukan "Simpan"
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memproses data", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

