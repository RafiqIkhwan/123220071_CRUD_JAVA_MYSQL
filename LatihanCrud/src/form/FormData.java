    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            String update = "UPDATE dataperpus SET judul = ?, genre = ?, penulis = ?, penerbit = ?, lokasi = ?, stock = ? WHERE id = ?";
            PreparedStatement pstmt = cn.prepareStatement(update);
            pstmt.setString(1, judul.getText());
            pstmt.setString(2, genre.getText());
            pstmt.setString(3, penulis.getText());
            pstmt.setString(4, penerbit.getText());
            pstmt.setString(5, lokasi.getText());
            pstmt.setString(6, stock.getText());
            pstmt.setString(7, id.getText());

            int rowsUpdated = pstmt.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                Bersih();
                TampilData();
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menyimpan data");
                TampilData();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data: " + ex.getMessage());
            TampilData();
        }

    }//GEN-LAST:event_editActionPerformed

