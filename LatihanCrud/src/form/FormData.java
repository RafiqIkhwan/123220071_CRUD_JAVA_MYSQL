    private void TampilData() {
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM dataperpus");

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("id");
            model.addColumn("judul");
            model.addColumn("genre");
            model.addColumn("penulis");
            model.addColumn("penerbit");
            model.addColumn("lokasi");
            model.addColumn("stock");

            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();

            model.setRowCount(0);

            while (rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("judul"),
                    rs.getString("genre"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getString("lokasi"),
                    rs.getString("stock")
                };

                model.addRow(data);
                tabel.setModel(model);
            }

        } catch (SQLException e) {

        }
    }

    private void cariData() {
        try {
            String columnName = combobox.getSelectedItem().toString();
            String query = "SELECT * FROM dataperpus WHERE " + columnName + " LIKE ?";
            PreparedStatement pstmt = cn.prepareStatement(query);
            pstmt.setString(1, "%" + cari.getText() + "%");
            rs = pstmt.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("id");
            model.addColumn("judul");
            model.addColumn("genre");
            model.addColumn("penulis");
            model.addColumn("penerbit");
            model.addColumn("lokasi");
            model.addColumn("stock");

            while (rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("judul"),
                    rs.getString("genre"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getString("lokasi"),
                    rs.getString("stock")
                };
                model.addRow(data);
            }
            tabel.setModel(model);
        } catch (SQLException ex) {

            // Handle exception appropriately, such as displaying an error message
        }

    }

